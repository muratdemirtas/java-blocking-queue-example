import java.util.concurrent.*;
public class rxThread implements Runnable{

    protected BlockingQueue queue = null;

    public rxThread(BlockingQueue queue) {
        this.queue = queue;
    }

    public void run() {
        try {
            System.out.println(queue.take());
            System.out.println(queue.take());
            System.out.println(queue.take());
        } catch (InterruptedException e) {
            e.printStackTrace();
        }
    }
}